/**
 * Process Phoenix marketing photos:
 *   - Reads JPGs from the source Marketing folder
 *   - Resizes to 1920px (full) and 480px (thumb) WebP
 *   - Writes to public/assets/phoenix/
 *   - Emits a JSON manifest at public/assets/phoenix/manifest.json
 *
 * Idempotent: skips files that already exist with the expected output name.
 * Run: node scripts/process-phoenix-photos.mjs
 */

import { promises as fs } from "node:fs";
import path from "node:path";
import sharp from "sharp";
import heicConvert from "heic-convert";

const SRC_DIR =
  "C:/Users/Dave/AI_BUILDS/Nursing-Rocks-Concerts-Live-Site - 3.0/public/assets/Marketing on NRCS Phoenix site";
const OUT_DIR = path.resolve("public/assets/phoenix");
const FULL_WIDTH = 1920;
const THUMB_WIDTH = 480;
const QUALITY = 82;

const slugify = (name) =>
  name
    .replace(/\.[^.]+$/, "")
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");

async function main() {
  await fs.mkdir(OUT_DIR, { recursive: true });

  const entries = await fs.readdir(SRC_DIR, { withFileTypes: true });
  const files = entries
    .filter((e) => e.isFile() && /\.(jpe?g|png|heic)$/i.test(e.name))
    .map((e) => e.name)
    .sort();

  console.log(`Found ${files.length} source files in ${SRC_DIR}`);

  const manifest = [];
  let processed = 0;
  let skipped = 0;

  for (const name of files) {
    const slug = slugify(name);
    const fullName = `${slug}.webp`;
    const thumbName = `${slug}-thumb.webp`;
    const fullPath = path.join(OUT_DIR, fullName);
    const thumbPath = path.join(OUT_DIR, thumbName);
    const srcPath = path.join(SRC_DIR, name);

    const [fullExists, thumbExists] = await Promise.all([
      fs.access(fullPath).then(() => true, () => false),
      fs.access(thumbPath).then(() => true, () => false),
    ]);

    if (fullExists && thumbExists) {
      skipped++;
    } else {
      try {
        // HEIC files need conversion to JPEG first (libheif lacks HEVC codec)
        let sharpInput = srcPath;
        let tmpBuf = null;
        if (/\.heic$/i.test(name)) {
          const heicBuf = await fs.readFile(srcPath);
          tmpBuf = await heicConvert({ buffer: heicBuf, format: "JPEG", quality: 0.95 });
          sharpInput = Buffer.from(tmpBuf);
        }
        const img = sharp(sharpInput, { failOn: "none" }).rotate(); // honor EXIF orientation
        await img
          .clone()
          .resize({ width: FULL_WIDTH, withoutEnlargement: true })
          .webp({ quality: QUALITY })
          .toFile(fullPath);
        await img
          .clone()
          .resize({ width: THUMB_WIDTH, withoutEnlargement: true })
          .webp({ quality: QUALITY })
          .toFile(thumbPath);
        processed++;
      } catch (err) {
        console.error(`  ❌ ${name}: ${err.message}`);
        continue;
      }
    }

    manifest.push({
      source: name,
      url: `/assets/phoenix/${fullName}`,
      thumbnail_url: `/assets/phoenix/${thumbName}`,
      alt_text: "Nursing Rocks Phoenix 2026",
      tags: ["phoenix"],
    });
  }

  await fs.writeFile(
    path.join(OUT_DIR, "manifest.json"),
    JSON.stringify(manifest, null, 2)
  );

  // Also emit a TS module so the frontend can import the manifest
  // without referencing /public (which Vite doesn't import from).
  const tsModulePath = path.resolve("client/src/lib/phoenix-photos.ts");
  await fs.mkdir(path.dirname(tsModulePath), { recursive: true });
  const tsBody = `// AUTO-GENERATED by scripts/process-phoenix-photos.mjs — do not edit by hand.\n\nexport interface PhoenixPhoto {\n  source: string;\n  url: string;\n  thumbnail_url: string;\n  alt_text: string;\n  tags: string[];\n}\n\nexport const phoenixPhotos: PhoenixPhoto[] = ${JSON.stringify(manifest, null, 2)};\n`;
  await fs.writeFile(tsModulePath, tsBody);

  console.log(`✅ Done. Processed: ${processed}, Skipped (already existed): ${skipped}`);
  console.log(`   Output: ${OUT_DIR}`);
  console.log(`   Manifest: ${path.join(OUT_DIR, "manifest.json")} (${manifest.length} entries)`);
  console.log(`   TS module: ${tsModulePath}`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
